public class Treino05 {
    
    public static void main(String[] args) {

        //! Modelos de declaração de variável:
        var login = "Ola!";
        var password = "123456789";

        // tipo nomeDaReferência = conteudoReferência
        String login1 = "Tudo bem?";
        int number1 = 10;
        double number2 = 12.57;
        boolean isDiva = true;

        int number3;
        number3 = 19;

        //! Tipo de variáveis:
        // Int -> inteiro
        // Double -> fracionado/decimal
        // String -> texto
        // Booleano -> true / false


        System.out.println(login);
        System.out.println(password);
        System.out.println(login1);
        System.out.println(number1);
        System.out.print(number2);
        System.out.println(isDiva);
        System.out.println(number3);
        System.out.println(number3 + 2);

        //! Métodos começam com letra minúscula!
    }
}
